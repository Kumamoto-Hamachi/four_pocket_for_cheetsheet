# dict
d = {"key1":1, "key2":2, "key3":3}
# dict for
for k in d:
    print(k)
    # key1
    # key2
    # key3
# dict for key
for k in d.keys:
    print(k)
    #上に同じ
# dict for values
for v in d.values:
    print(v)
    # 1
    # 2
    # 3
for k, v in d.items:
    print(k, v)
    # key1 1
    # key2 2
    # key3 3




